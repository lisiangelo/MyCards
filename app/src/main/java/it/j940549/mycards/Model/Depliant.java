package it.j940549.mycards.Model;

public class Depliant {

     String url;
     String pathImage;
     String nomeDepliant;

    public Depliant(){

        }

    public String getPathImage() {
        return pathImage;
    }

    public void setPathImage(String pathImage) {
        this.pathImage = pathImage;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getNomeDepliant() {
        return nomeDepliant;
    }

    public void setNomeDepliant(String nomeDepliant) {
        this.nomeDepliant = nomeDepliant;
    }
}
